# 1,2,3 더하기
# 정수 n을 1, 2, 3 의 합으로 나타내는 방법의 수

dp = [0] * 12
dp[1] = 1 # 1
dp[2] = 2 # 1 1, 2
dp[3] = 4 # 1 1 1, 1 2, 2 1, 3
dp[4] = 7 # 1 1 1 1, 1 1 2, 1 2 1, 2 1 1, 2 2, 1 3, 3 1
dp[5] = 13# 11111, 1112, 1121, 1211, 2111,113,131,311,122,221,212,23,32
# 1
# 1 + 1
# 1 + 2 + 1
# 1 + 2 + 3 + 1
# 1 + 4 + 3 + 3 + 2 /5
# 1 + 5 + 6 + 4 + 6 + 1 + 1 /6 24
# /7
for i in range(4,12):
    dp[i] = dp[i-1] + dp[i-2] + dp[i-3]

T = int(input())
for tc in range(1,T+1):
    n = int(input())
    print(dp[n])
